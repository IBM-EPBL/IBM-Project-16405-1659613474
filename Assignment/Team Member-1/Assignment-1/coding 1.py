

list=[1,2,3,4,5,6,7]
print(list)

#insertion
list.insert(5,13)
print(list)

#deletion of first occurence of integer
del list[2]
print(list)

#insertion at end of list
list.append(-5)
print(list)

#sorting
list.sort()
print(list)

#popping last element
list.pop()
print(list)

#reverse the list
list.reverse()
print(list)

